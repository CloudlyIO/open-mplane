//=============================================================================================================
// PUBLIC
//=============================================================================================================

//-------------------------------------------------------------------------------------------------------------
SingleBootEnv::~SingleBootEnv()
{
}

//=============================================================================================================
// PROTECTED CONSTRUCTOR
//=============================================================================================================

//-------------------------------------------------------------------------------------------------------------
SingleBootEnv::SingleBootEnv(bool deferInit) :
	BootEnv(0, false, deferInit)
{
	setMinBanks(1) ;
}


//=============================================================================================================
// PROTECTED HOOKS
//=============================================================================================================

//-------------------------------------------------------------------------------------------------------------
bool SingleBootEnv::writeEnv(const std::map<std::string, std::string>& newEnv)
{
	return writeActive(newEnv) ;
}
